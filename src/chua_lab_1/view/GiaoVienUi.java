    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:

        String ten = txtHoTen.getText();
        String sdt = txtSdt.getText();
        String luong = txtLuong.getText();
        int gioiTinh = 0;
        if (rdNu.isSelected()) {
            gioiTinh = 1;
        }

        GiaoVien giaoVien = new GiaoVien();
        giaoVien.setHoTen(ten);
        giaoVien.setLuong(Float.parseFloat(luong));
        giaoVien.setSdt(sdt);
        giaoVien.setGioiTinh(gioiTinh);

        if (giaoVienService.addNew(giaoVien)) {
            JOptionPane.showMessageDialog(this, "Them thanh cong");
            loadData(giaoVienService.getList());
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tbGiaoVien.getSelectedRow();
        if (giaoVienService.xoa(index)) {
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
            loadData(giaoVienService.getList());
        }
    }//GEN-LAST:event_btnXoaActionPerformed

