    public void loadData(ArrayList<GiaoVien> list) {
        defaultTableModel = (DefaultTableModel) tbGiaoVien.getModel();
        defaultTableModel.setRowCount(0);
        for (GiaoVien giaoVien : list) {
            defaultTableModel.addRow(new Object[]{
                giaoVien.getHoTen(), giaoVien.getSdt(),
                giaoVien.gioiTinh(), giaoVien.getLuong(), giaoVien.xepLoai()
            });
        }
    }

